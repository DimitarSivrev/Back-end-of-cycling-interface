package cycling;

/**
 * This enum is used to represent the different classes.
 * 
 * @author Dimitar Sivrev
 * @version 1.0
 *
 */
public enum ClassType {

	/**
	 * Used for races in the cycling tornament.
	 */
	Race,

	/**
	 * Used for stages of races in the cycling tornament.
	 */
	Stage,

	/**
	 * Used for segments of stages in the cycling tornament.
	 */
	Segment,

	/**
	 * Used for teams in the cycling tornament.
	 */
	Team,

	/**
	 * Used for riders of teams in the cycling tornament.
	 */
	Rider;
}